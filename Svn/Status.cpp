Status::Status()
:SvnCommand("Status", MSG_STATUS, R_Status)
{
	TRACE_METHOD ((CC_APPLICATION, REPORT_METHOD));
}

Status::~Status()
{
	TRACE_METHOD ((CC_APPLICATION, REPORT_METHOD));
}


///////////////////////////////////////////////////////////////////////////////
// -- Services
///////////////////////////////////////////////////////////////////////////////
void Status::Execute()
{
	TRACE_METHOD ((CC_APPLICATION, REPORT_METHOD));
	
	ResultsWindow* pWindow = new ResultsWindow(GetName());
	m_pTarget = pWindow;
	
	ExecuteSvn(string("svn status"));
}


